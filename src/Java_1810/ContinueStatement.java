package Java_1810;

public class ContinueStatement {

	public static void main(String[] args) {
	   
		/** It is used to skips the remaining code at the specified condition.*/
		
		for (int i=0; i<=5; i++){
			
			if(i==4){
				
				continue;
				
			}
			
			System.out.println(i*3);
			
		}
		
		
	}

}
